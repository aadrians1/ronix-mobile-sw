	BSF	PORTA,1
	CLRF	INTCON
	MOVLW	0E0H
	MOVWF	RTCC
	MOVLW	B'10000000'
	OPTION
	INCF	TMRL
	MOVFW	TMPW
	BCF	PORTA,1
	RETURN

MAIN	MOVLW	0h
	TRIS	PORTB
	TRIS	PORTA
	CLRF	PORTB
	CLRF	PORTA
	BTFSS	PORTA,0
	BSF	PORTA,0
	BTFSC	PORTA,0
	BCF	PORTA,0
	CLRF	RTCC
	BSF	STATUS,5
	MOVLW	B'10000000'
	MOVWF	OPTN
	BCF	STATUS,5
	MOVLW	B'10100000'
	MOVWF	INTCON


