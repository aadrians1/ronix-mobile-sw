COMWRIN		EQU 	B'10100000'
COMRDIN		EQU	B'10100001'
ADDRIN		EQU	B'10110000'

COMWROUT	EQU	B'10101100'
ADDR1		EQU	B'11001101'
ADDR2		EQU	B'11001110'
ADDR3		EQU	B'11001111'
ADDR4		EQU	B'11010000'

SNWROUT		EQU	B'10101110'
ADDRSN		EQU	9Ch		;ADRESS SEREAL NO.	(MAYBE ADDRES 8Ch)

ADDRSYS		EQU	0D2h		;ADRESS SYS CODE,MAST BE USED WITH COMWROUT


;	PARAMETRS LEGAL
LDAT1		EQU	43h	;1&2 DIGITS OF NOMER
LDAT2		EQU	44h
LDAT3		EQU	53h
LDAT4		EQU	90h	;7&8 DIGITS OF NOMER

LSYS		EQU	3Ah	;LEGAL 2&3 DIGITS OF SYS CODE
NSYS		EQU	87h	;NOT LEGAL 2&3 DIGITS OF SYS CODE

LSENO		EQU 	38h	;LEGAL 5 DIGIT OF SEREAL NOMER 
NSENO		EQU	35h	;NOT LEGAL 5 DIGIT OF SER. NO.


STATUS		EQU	03h
PORTB		EQU	06h
TRISB		EQU	06h

;EEDATA		EQU	08h
;EEADR		EQU	09h
;EECON1		EQU	08h
;EECON2		EQU	09h

WRB		EQU	0Ch
REB		EQU	0Dh
CNTCIKL		EQU	0Eh
TIME		EQU	0Fh
TIME2		EQU	10H

DATA1		EQU	11h
DATA2		EQU	12h
DATA3		EQU	13h
DATA4		EQU	14h

SYS		EQU	15h
SENO		EQU	16h




;
	ORG	0
	CLRF	PORTB


	CALL	CLDRESE

	CALL	TM
	CALL 	TM
	CALL	START
	MOVLW	COMWRIN
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDRIN
	MOVWF	WRB
	CALL	OUTI2C
	CALL	START
	MOVLW	COMRDIN
	MOVWF	WRB
	CALL	OUTI2C
	CALL	INI2C
	MOVF	REB,0
	MOVWF	DATA1
	CALL	INI2C
	MOVF	REB,0
	MOVWF	DATA2
	CALL	INI2C
	MOVF	REB,0
	MOVWF	DATA3
	CALL	INI2C
	MOVF	REB,0
	MOVWF	DATA4
	CALL	STOP
	CALL	TM		;END PROC READING 24C16


	BTFSC	DATA1,0
	GOTO	NLEG
	BTFSC	DATA1,1
	GOTO	NLEG
	BTFSC	DATA1,2
	GOTO	NLEG
	BTFSC	DATA1,3
	GOTO	NLEG
	BTFSC	DATA1,4
	GOTO	NLEG
	BTFSC	DATA1,5
	GOTO	NLEG
	BTFSC	DATA1,6
	GOTO	NLEG
	BTFSC	DATA1,7
	GOTO	NLEG
	GOTO	LEG	

	;WRITE EEPROM NOT LEGAL

NLEG	CALL	CLDREF
	MOVLW	NSYS
	MOVWF	SYS
	MOVLW	NSENO
	MOVWF	SENO
	GOTO	CONWR
	
	;WRITE EEPROM LEGAL

LEG	MOVLW	LDAT1
	MOVWF	DATA1

	MOVLW	LDAT2
	MOVWF	DATA2

	MOVLW	LDAT3
	MOVWF	DATA3

	MOVLW	LDAT4
	MOVWF	DATA4
	
	MOVLW	LSYS
	MOVWF	SYS

	MOVLW	LSENO
	MOVWF	SENO

	
;------------------------------------WRITE SYS CODE------------------

CONWR	CALL	TM

	CALL	START
	MOVLW	COMWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDRSYS
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	SYS,0
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP

	CALL	TM
;------------------------------------WRITE SER. NOM.-------------------
	CALL	START
	MOVLW	SNWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDRSN
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	SENO,0
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP
	
	CALL	TM

	CALL	START
	MOVLW	SNWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDRSN
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	SENO,0
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP
	CALL	TM

	CALL	START		;--------------WRITE SYS CODE-----------
	MOVLW	COMWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDRSYS
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	SYS,0
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP
	CALL	TM
;-------------------------------WRITE TEL NO.-------------------

	CALL	START
	MOVLW	COMWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDR1
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	DATA1,0
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP

	CALL	TM

	CALL	START
	MOVLW	COMWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDR2
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	DATA2,0
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP

	CALL	TM

	CALL	START
	MOVLW	COMWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDR3
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	DATA3,0
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP
	
	CALL	TM

	CALL	START
	MOVLW	COMWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDR4
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	DATA4,0
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP
	
	CALL	TM
	CALL	TM
	CALL	SETRESE
	GOTO	$
	GOTO	$
;--------------------------------------------------------------MAIN

OUTI2C	MOVLW	8
	MOVWF	CNTCIKL
CONOUT	BTFSC	WRB,7
	CALL	SETDAT
	CALL	TM
	CALL	SETSTR
	CALL	TM	
	CALL	CLDSTR
	CALL	TM
	CALL	CLDDAT
	RLF	WRB,1
	DECFSZ	CNTCIKL,1
	GOTO	CONOUT
	
	CALL	SETDAT
	CALL	TM
	CALL	SETSTR
	CALL	TM	
	CALL	CLDSTR
	CALL	TM
	CALL	CLDDAT
	CALL	TM
	RETURN
;--------OUT-------------------------------------------------OUT----------
INI2C	CLRF	REB
	MOVLW	8
	MOVWF	CNTCIKL
	CALL	SETDAT
	CALL	TM
	
CONIN	RLF	REB,1
	CALL	SETSTR
	CALL	TM
	BTFSC	PORTB,0
	BSF	REB,0		;WRITE LOW BIT TO REB
	CALL	CLDSTR
	CALL	TM
	DECFSZ	CNTCIKL,1
	GOTO	CONIN
	CALL	CLDDAT
	CALL	TM
	CALL	SETSTR	
	CALL	TM
	CALL	CLDSTR
	CALL	TM
	RETURN
;----------------IN------------------------------------------------IN-------

CLDREF	MOVLW	32		;DISABLE REFERENS CODE (BIT OF PORT = 0)
	MOVWF	STATUS
	BCF	TRISB,2		;USER PORT (connect on 50vivod ZC98302fu) 
	MOVLW	0
	MOVWF	STATUS
	RETURN


SETSTR	MOVLW	32
	MOVWF	STATUS
	BSF	TRISB,1
	MOVLW	0
	MOVWF	STATUS
	RETURN

CLDSTR	MOVLW	32
	MOVWF	STATUS
	BCF	TRISB,1
	MOVLW	0
	MOVWF	STATUS
	RETURN

SETDAT	MOVLW	32
	MOVWF	STATUS
	BSF	TRISB,0
	MOVLW	0
	MOVWF	STATUS
	RETURN

CLDDAT	MOVLW	32
	MOVWF	STATUS
	BCF	TRISB,0
	MOVLW	0
	MOVWF	STATUS
	RETURN

SETRESE	MOVLW	32
	MOVWF	STATUS
	BSF	TRISB,3
	MOVLW	0
	MOVWF	STATUS
	RETURN

CLDRESE	MOVLW	32
	MOVWF	STATUS
	BCF	TRISB,3
	MOVLW	0
	MOVWF	STATUS
	RETURN

;-----------------PORT-------------------------------------------------------
START	CALL	SETDAT
	CALL	TM
	CALL	SETSTR
	CALL	TM
	CALL	CLDDAT
	CALL	TM
	CALL	CLDSTR
	CALL	TM
	RETURN
;-------------------START---------------------------------------------------
STOP	CALL	SETSTR
	CALL	TM
	CALL	SETDAT
	CALL	TM
	RETURN
;---------------------STOP---------------------------------------------------	
TM	MOVLW	07fh
	MOVWF	TIME
LL3	DECFSZ	TIME,1
	GOTO	LL3
	RETURN
;----------------------TM----------------------------------------------------


TMLONG	MOVLW	08FH
	MOVWF	TIME
LL1	MOVLW	0FFH
	MOVWF	TIME2
LL2	DECFSZ	TIME2,1
	GOTO	LL2	

	DECFSZ	TIME,1
	GOTO	LL1
	RETURN	
;----------------------TMLONG-----------------------------------------------


