LIST P=16C54		;load\save with reset for N-250 (SIS)

COMWRIN		EQU 	B'10100110'
COMRDIN		EQU	B'10100111'
ADDRIN		EQU	B'11110000'

COMWROUT	EQU	B'10101000'
ADDR1		EQU	B'01111010'
ADDR2		EQU	B'01111011'
ADDR3		EQU	B'01111100'
ADDR4		EQU	B'01111101'
ADDR5		EQU	B'01111110'
ADDR6		EQU	B'01111111'



STATUS		EQU	03h
PORTB		EQU	06h

WRB		EQU	07h
REB		EQU	08h
CNTCIKL		EQU	09h
TIME		EQU	0Ah
TIME2		EQU	0BH

DATA1		EQU	0Ch
DATA2		EQU	0Dh
DATA3		EQU	0Eh
DATA4		EQU	0Fh
DATA5		EQU	10h
TMPF		EQU	11h

	ORG	0
	MOVLW	0FFh
	MOVWF	TMPF
	TRIS	PORTB
	CLRF	PORTB
	CALL	CLDRESE
	CALL	TMLONG

	CALL	START
	MOVLW	COMWRIN
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDRIN
	MOVWF	WRB
	CALL	OUTI2C
	CALL	START

	MOVLW	COMRDIN
	MOVWF	WRB
	CALL	OUTI2C
	CALL	INI2C
	MOVF	REB,0
	MOVWF	DATA1
	CALL	INI2C
	MOVF	REB,0
	MOVWF	DATA2
	CALL	INI2C
	MOVF	REB,0
	MOVWF	DATA3
	CALL	INI2C
	MOVF	REB,0
	MOVWF	DATA4
	CALL	INI2C
	MOVF	REB,0
	MOVWF	DATA5
	CALL	STOP
	CALL	TMLONG

	CALL	START
	MOVLW	COMWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDR1
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	DATA1,0
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP

	CALL	TM

	CALL	START
	MOVLW	COMWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDR2
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	DATA2,0
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP

	CALL	TM

	CALL	START
	MOVLW	COMWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDR3
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	DATA3,0
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP
	
	CALL	TM

	CALL	START
	MOVLW	COMWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDR4
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	DATA4,0
	ANDLW	0F0h
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP

	CALL	TM

	CALL	START
	MOVLW	COMWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDR5
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	DATA4,0
	ANDLW	0Fh
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP

	CALL	TM

	CALL	START
	MOVLW	COMWROUT		
	MOVWF	WRB
	CALL	OUTI2C
	MOVLW	ADDR6
	MOVWF	WRB
	CALL	OUTI2C
	MOVF	DATA5,0
	MOVWF	WRB
	CALL	OUTI2C
	CALL	STOP

	CALL	SETRESE
SLP	MOVLW	0FFh
	TRIS	PORTB
	SLEEP
	GOTO	SLP
;--------------------------------------------------------------MAIN

OUTI2C	MOVLW	8
	MOVWF	CNTCIKL
CONOUT	BTFSC	WRB,7
	CALL	SETDAT
	CALL	TM
	CALL	SETSTR
	CALL	TM	
	CALL	CLDSTR
	CALL	TM
	CALL	CLDDAT
	RLF	WRB,1
	DECFSZ	CNTCIKL,1
	GOTO	CONOUT
	
	CALL	SETDAT
	CALL	TM
	CALL	SETSTR
	CALL	TM	
	CALL	CLDSTR
	CALL	TM
	CALL	CLDDAT
	CALL	TM
	RETLW	0
;--------OUT-------------------------------------------------OUT----------
INI2C	CLRF	REB
	MOVLW	8
	MOVWF	CNTCIKL
	CALL	SETDAT
	CALL	TM
	
CONIN	RLF	REB,1
	CALL	SETSTR
	CALL	TM
	BTFSC	PORTB,0
	BSF	REB,0		;WRITE LOW BIT TO REB
	CALL	CLDSTR
	CALL	TM
	DECFSZ	CNTCIKL,1
	GOTO	CONIN
	CALL	CLDDAT
	CALL	TM
	CALL	SETSTR	
	CALL	TM
	CALL	CLDSTR
	CALL	TM
	RETLW	0
;----------------IN------------------------------------------------IN-------
SETSTR	BSF	TMPF,1
	MOVFW	TMPF
	TRIS	PORTB
	RETLW	0

CLDSTR	BCF	TMPF,1
	MOVFW	TMPF
	TRIS	PORTB
	RETLW	0

SETDAT	BSF	TMPF,0
	MOVFW	TMPF
	TRIS	PORTB
	RETLW	0

CLDDAT	BCF	TMPF,0
	MOVFW	TMPF
	TRIS	PORTB
	RETLW	0

SETRESE	BSF	TMPF,3
	MOVFW	TMPF
	TRIS	PORTB
	RETLW	0

CLDRESE	BCF	TMPF,3
	MOVFW	TMPF
	TRIS	PORTB
	RETLW	0

START	CALL	SETDAT
	CALL	TM
	CALL	SETSTR
	CALL	TM
	CALL	CLDDAT
	CALL	TM
	CALL	CLDSTR
	CALL	TM
	RETLW	0
;-------------------START---------------------------------------------------
STOP	CALL	SETSTR
	CALL	TM
	CALL	SETDAT
	CALL	TM
	RETLW	0
;---------------------STOP---------------------------------------------------	
TM	MOVLW	0BFh
	MOVWF	TIME
LL3	DECFSZ	TIME,1
	GOTO	LL3
	RETLW	0
;----------------------TM----------------------------------------------------


TMLONG	MOVLW	1FH
	MOVWF	TIME
LL1	MOVLW	0FFH
	MOVWF	TIME2
LL2	DECFSZ	TIME2,1
	GOTO	LL2	

	DECFSZ	TIME,1
	GOTO	LL1
	RETLW	0	
;----------------------TMLONG-----------------------------------------------
end
        
