
LIST P=16C84		;brelok for beeper QUARZ=4Mhz F=425Khz

TIME_1		EQU	3Ch
TIME_2		EQU	3Ch

STATUS		EQU	03h
PORTB		EQU	06h
TRISB		EQU	06h

WRB		EQU	0Ch
REB		EQU	0Dh
CNTCIKL		EQU	0Eh
TIME		EQU	0Fh
TIME2		EQU	10H

	ORG	0
	MOVLW	32
	MOVWF	STATUS
	MOVLW	0
	MOVWF	TRISB
	MOVWF	STATUS

BEEP	MOVLW	0Fh
	MOVWF	PORTB
	NOP
	NOP
	CALL	TML
	MOVLW	0F0h
	MOVWF	PORTB
	CALL	TML
	GOTO BEEP

	MOVLW	0
	MOVWF	PORTB
	CALL	TML
	GOTO	BEEP
;----------------------TML---------------------------------------------------
TML	MOVLW	TIME_2
	MOVWF	TIME
LL1	MOVLW	05h
	MOVWF	TIME2
LL2	DECFSZ	TIME2,1
	GOTO	LL2	
	DECFSZ	TIME,1
	GOTO	LL1
	RETURN	
;----------------------TM----------------------------------------------------
TM	MOVLW	TIME_1
	MOVWF	TIME
TTTM	DECFSZ	TIME,1
	GOTO	TTTM
	RETURN
end
